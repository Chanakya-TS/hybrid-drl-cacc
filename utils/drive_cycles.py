def get_cycle_trajectory(
    cycle_name: str,
    dt: float = 0.05
) -> Tuple[np.ndarray, float]:
    """
    Get an interpolated drive cycle trajectory in m/s.

    Interpolates from the 1-second EPA data to the simulation timestep (dt)
    and converts from mph to m/s.

    Args:
        cycle_name: One of 'udds', 'hwfet', 'us06'
        dt: Simulation timestep in seconds (default 0.05)

    Returns:
        trajectory: Speed array in m/s at intervals of dt
        duration_s: Total cycle duration in seconds
    """
    cycle_name = cycle_name.lower()
    if cycle_name not in _CYCLE_RAW:
        available = ', '.join(_CYCLE_RAW.keys())
        raise ValueError(f"Unknown cycle '{cycle_name}'. Available: {available}")

    raw_mph = np.array(_CYCLE_RAW[cycle_name], dtype=np.float64)
    n_points = len(raw_mph)
    duration_s = float(n_points - 1)  # e.g. 1370 points → 1369s

    # Original time grid: 1-second intervals
    t_original = np.arange(n_points, dtype=np.float64)

    # Interpolated time grid at simulation dt
    t_interp = np.arange(0, duration_s + dt * 0.5, dt)

    # Linear interpolation from 1s → dt resolution
    speed_mph_interp = np.interp(t_interp, t_original, raw_mph)

    # Convert mph → m/s
    trajectory = speed_mph_interp * MPH_TO_MS

    return trajectory, duration_s


def get_cycle_subsections(
    cycle_name: str,
    dt: float = 0.05,
    window_s: float = 300.0,
    overlap_s: float = 30.0
) -> List[Tuple[np.ndarray, float, str]]:
    """
    Split a drive cycle into overlapping subsections for DRL training.

    Full EPA cycles are too long for individual training episodes
    (e.g. UDDS = 27,380 sim steps at dt=0.05). This function creates
    manageable ~300s windows with overlap for continuity.

    Args:
        cycle_name: One of 'udds', 'hwfet', 'us06'
        dt: Simulation timestep in seconds
        window_s: Window duration in seconds (default 300)
        overlap_s: Overlap between windows in seconds (default 30)

    Returns:
        List of (trajectory, duration_s, subsection_name) tuples
    """
    full_trajectory, total_duration = get_cycle_trajectory(cycle_name, dt)

    window_steps = int(window_s / dt)
    overlap_steps = int(overlap_s / dt)
    stride_steps = window_steps - overlap_steps

    subsections = []
    start = 0
    idx = 0

    while start < len(full_trajectory):
        end = min(start + window_steps, len(full_trajectory))
        segment = full_trajectory[start:end]
        seg_duration = (len(segment) - 1) * dt

        name = f"{cycle_name}_s{idx}"
        subsections.append((segment, seg_duration, name))

        start += stride_steps
        idx += 1

        # Stop if the next window would be too short (< 50% of window)
        if start >= len(full_trajectory) - window_steps // 2:
            break

    return subsections
